/// Generates the helper functions each storage-provider module uses to read
/// and write the in-memory `Table`s. The four modules below differ only in the
/// serde codec (`serde_json` vs `postcard`), its error type, and which trait
/// crate/version (`CURRENT_VERSION` from the current or the previous line) the
/// `Key`/`Entity` bounds come from — so the bodies are generated from this one
/// definition. Each module `use`s the matching `Key`/`Entity`/`traits`/`Table`
/// before invoking it. The `virtual-clients-draft` helpers are always generated
/// but unused where the corresponding `StorageProvider` impl does not include the
/// vc-draft methods (allowed by the crate-level `#![allow(dead_code)]`).
macro_rules! storage_helpers {
    ($version:expr, $err:ty, $ser:path, $de:path) => {
        fn write<K: Key<$version>, E: Entity<$version>>(
            key: &K,
            value: &E,
            data: &mut Table,
        ) -> Result<(), $err> {
            let key = $ser(key)?;
            let value = $ser(value)?;
            let _ = data.insert(key, value);

            Ok(())
        }

        fn read<K: Key<$version>, E: Entity<$version>>(
            key: &K,
            data: &Table,
        ) -> Result<Option<E>, $err> {
            let key = $ser(key)?;

            if let Some(data) = data.get(&key) {
                let value: E = $de(data)?;

                Ok(Some(value))
            } else {
                Ok(None)
            }
        }

        fn delete<K: Key<$version>>(key: &K, data: &mut Table) -> Result<(), $err> {
            let key = $ser(key)?;

            let _ = data.remove(&key);

            Ok(())
        }

        /// Appends a value to the list stored under `key`.
        fn append<K: Key<$version>, E: Entity<$version>>(
            key: &K,
            value: &E,
            data: &mut Table,
        ) -> Result<(), $err> {
            let key = $ser(key)?;
            let mut list: Vec<Vec<u8>> = match data.get(&key) {
                Some(bytes) => $de(bytes)?,
                None => Vec::new(),
            };
            list.push($ser(value)?);
            let _ = data.insert(key, $ser(&list)?);

            Ok(())
        }

        /// Reads the list of values stored under `key`, or an empty vector if none.
        fn read_list<K: Key<$version>, E: Entity<$version>>(
            key: &K,
            data: &Table,
        ) -> Result<Vec<E>, $err> {
            let key = $ser(key)?;
            let list: Vec<Vec<u8>> = match data.get(&key) {
                Some(bytes) => $de(bytes)?,
                None => return Ok(Vec::new()),
            };

            list.iter().map(|bytes| $de(bytes)).collect()
        }

        /// Stores a proposal keyed by `(group_id, proposal_ref)` and appends the
        /// reference to the per-group proposal queue.
        fn store_proposal<
            GroupId: Key<$version>,
            ProposalRef: Key<$version> + Entity<$version>,
            QueuedProposal: Entity<$version>,
        >(
            group_id: &GroupId,
            proposal_ref: &ProposalRef,
            proposal: &QueuedProposal,
            proposals: &mut Table,
            proposal_refs: &mut Table,
        ) -> Result<(), $err> {
            let proposal_key = $ser(&(group_id, proposal_ref))?;
            let _ = proposals.insert(proposal_key, $ser(proposal)?);

            let refs_key = $ser(group_id)?;
            let mut refs: Vec<Vec<u8>> = match proposal_refs.get(&refs_key) {
                Some(bytes) => $de(bytes)?,
                None => Vec::new(),
            };
            refs.push($ser(proposal_ref)?);
            let _ = proposal_refs.insert(refs_key, $ser(&refs)?);

            Ok(())
        }

        /// Reads all queued proposals (reference and proposal) for a group.
        fn read_queued_proposals<
            GroupId: Key<$version>,
            ProposalRef: Key<$version> + Entity<$version>,
            QueuedProposal: Entity<$version>,
        >(
            group_id: &GroupId,
            proposals: &Table,
            proposal_refs: &Table,
        ) -> Result<Vec<(ProposalRef, QueuedProposal)>, $err> {
            let refs_key = $ser(group_id)?;
            let refs: Vec<Vec<u8>> = match proposal_refs.get(&refs_key) {
                Some(bytes) => $de(bytes)?,
                None => return Ok(Vec::new()),
            };

            refs.iter()
                .map(|ref_bytes| {
                    let proposal_ref: ProposalRef = $de(ref_bytes)?;
                    let proposal_key = $ser(&(group_id, &proposal_ref))?;
                    let proposal_bytes = proposals.get(&proposal_key).unwrap();
                    let proposal: QueuedProposal = $de(proposal_bytes)?;
                    Ok((proposal_ref, proposal))
                })
                .collect()
        }

        /// Removes a single proposal and its reference from the queue.
        fn remove_queued_proposal<GroupId: Key<$version>, ProposalRef: Key<$version>>(
            group_id: &GroupId,
            proposal_ref: &ProposalRef,
            proposals: &mut Table,
            proposal_refs: &mut Table,
        ) -> Result<(), $err> {
            let proposal_key = $ser(&(group_id, proposal_ref))?;
            let _ = proposals.remove(&proposal_key);

            let refs_key = $ser(group_id)?;
            if let Some(bytes) = proposal_refs.get(&refs_key) {
                let mut refs: Vec<Vec<u8>> = $de(bytes)?;
                let target = $ser(proposal_ref)?;
                refs.retain(|item| item != &target);
                let _ = proposal_refs.insert(refs_key, $ser(&refs)?);
            }

            Ok(())
        }

        /// Clears all proposals and references for a group.
        fn clear_proposals<
            GroupId: Key<$version>,
            ProposalRef: Key<$version> + Entity<$version>,
        >(
            group_id: &GroupId,
            proposals: &mut Table,
            proposal_refs: &mut Table,
        ) -> Result<(), $err> {
            let refs_key = $ser(group_id)?;
            if let Some(bytes) = proposal_refs.get(&refs_key) {
                let refs: Vec<Vec<u8>> = $de(bytes)?;
                for ref_bytes in &refs {
                    let proposal_ref: ProposalRef = $de(ref_bytes)?;
                    let proposal_key = $ser(&(group_id, &proposal_ref))?;
                    let _ = proposals.remove(&proposal_key);
                }
            }
            let _ = proposal_refs.remove(&refs_key);

            Ok(())
        }

        /// Writes the HPKE key pairs for a `(group_id, epoch, leaf_index)` slot.
        fn write_epoch_key_pairs<
            GroupId: Key<$version>,
            EpochKey: Key<$version>,
            HpkeKeyPair: Entity<$version>,
        >(
            group_id: &GroupId,
            epoch: &EpochKey,
            leaf_index: u32,
            key_pairs: &[HpkeKeyPair],
            data: &mut Table,
        ) -> Result<(), $err> {
            let key = $ser(&(group_id, epoch, leaf_index))?;
            let _ = data.insert(key, $ser(key_pairs)?);

            Ok(())
        }

        /// Reads the HPKE key pairs for a `(group_id, epoch, leaf_index)` slot.
        fn read_epoch_key_pairs<
            GroupId: Key<$version>,
            EpochKey: Key<$version>,
            HpkeKeyPair: Entity<$version>,
        >(
            group_id: &GroupId,
            epoch: &EpochKey,
            leaf_index: u32,
            data: &Table,
        ) -> Result<Vec<HpkeKeyPair>, $err> {
            let key = $ser(&(group_id, epoch, leaf_index))?;
            match data.get(&key) {
                Some(bytes) => $de(bytes),
                None => Ok(Vec::new()),
            }
        }

        /// Deletes the HPKE key pairs for a `(group_id, epoch, leaf_index)` slot.
        fn delete_epoch_key_pairs<GroupId: Key<$version>, EpochKey: Key<$version>>(
            group_id: &GroupId,
            epoch: &EpochKey,
            leaf_index: u32,
            data: &mut Table,
        ) -> Result<(), $err> {
            let key = $ser(&(group_id, epoch, leaf_index))?;
            let _ = data.remove(&key);

            Ok(())
        }

        /// Whether any retained key package material still references `epoch_id`.
        #[cfg(feature = "virtual-clients-draft")]
        fn epoch_is_referenced<EpochId: Key<$version>>(
            epoch_id: &EpochId,
            epoch_tags: &Table,
        ) -> Result<bool, $err> {
            let serialized = $ser(epoch_id)?;
            Ok(epoch_tags.values().any(|value| value == &serialized))
        }

        /// Writes an advanced operation tree together with the retained key package
        /// material it produced, tagging each material with its epoch.
        #[cfg(feature = "virtual-clients-draft")]
        fn write_retained_batch<
            EpochId: Key<$version>,
            VcOperationTree: Entity<$version>,
            KeyPackageRef: Key<$version>,
            RetainedKeyPackageMaterial: Entity<$version>,
        >(
            epoch_id: &EpochId,
            operation_tree: &VcOperationTree,
            materials: &[(KeyPackageRef, RetainedKeyPackageMaterial)],
            operation_trees: &mut Table,
            material_table: &mut Table,
            epoch_tags: &mut Table,
        ) -> Result<(), $err> {
            let serialized_epoch = $ser(epoch_id)?;
            let _ = operation_trees.insert(serialized_epoch.clone(), $ser(operation_tree)?);
            for (hash_ref, material) in materials {
                let ref_key = $ser(hash_ref)?;
                let _ = material_table.insert(ref_key.clone(), $ser(material)?);
                let _ = epoch_tags.insert(ref_key, serialized_epoch.clone());
            }

            Ok(())
        }

        /// Deletes the emulation state and operation tree for `epoch_id` if no
        /// retained key package material still references it.
        #[cfg(feature = "virtual-clients-draft")]
        fn delete_vc_state_if_unreferenced<EpochId: Key<$version>>(
            epoch_id: &EpochId,
            epoch_states: &mut Table,
            operation_trees: &mut Table,
            epoch_tags: &Table,
        ) -> Result<bool, $err> {
            let serialized = $ser(epoch_id)?;
            if epoch_tags.values().any(|value| value == &serialized) {
                return Ok(false);
            }
            let _ = epoch_states.remove(&serialized);
            let _ = operation_trees.remove(&serialized);

            Ok(true)
        }
    };
}

