//! In-memory storage providers for the migration compat tests.
//!
//! [`StorageProviderState`] holds the group state as a set of in-memory
//! `Table`s (`HashMap<Vec<u8>, Vec<u8>>`, keyed and valued by serialized bytes),
//! shared behind an `Arc<Mutex<_>>` so a provider can be handed out repeatedly for
//! the same underlying state. Two thin views wrap it, one per serde codec:
//!
//! - [`PostcardProvider`] — a **non-self-describing** (binary) codec, standing in
//!   for a previous version's on-disk format.
//! - [`SerdeJsonProvider`] — a **self-describing** codec, used as the migration
//!   bridge format and the current version's store.
//!
//! Each is paired with a libcrux crypto/rand provider into a full
//! `OpenMlsProvider` (the `*OpenMlsProvider` structs) so real group operations can
//! run against it — in both the current-version and previous-version (`_compat`)
//! flavours, since a migration test drives both lines.
//!
//! Each backing view implements the `StorageProvider` trait for every
//! (codec × trait-version) combination it needs:
//!
//! | module              | codec        | `StorageProvider` version         |
//! |---------------------|--------------|-----------------------------------|
//! | `serde_json_current`| `serde_json` | current crate                     |
//! | `serde_json_compat` | `serde_json` | previous-version crate (`_compat`)|
//! | `postcard_current`  | `postcard`   | current crate                     |
//! | `postcard_compat`   | `postcard`   | previous-version crate (`_compat`)|
//!
//! The trait methods are generated by the `impl_storage_provider_*!` macros and
//! the free `Table` read/write helpers by the `storage_helpers!` macro, so each
//! module only supplies its codec, error type, and trait imports.

#![allow(dead_code, unused)]

use std::{
    collections::HashMap,
    sync::{Arc, Mutex},
};

type Table = HashMap<Vec<u8>, Vec<u8>>;

#[derive(Default)]
struct Data {
    // group state
    join_config: Table,
    own_leaf_nodes: Table,
    queued_proposals: Table,
    proposal_queue_refs: Table,
    tree: Table,
    interim_transcript_hash: Table,
    group_context: Table,
    confirmation_tag: Table,
    group_state: Table,
    message_secrets: Table,
    resumption_psk_store: Table,
    own_leaf_index: Table,
    group_epoch_secrets: Table,
    encryption_epoch_key_pairs: Table,

    // crypto objects
    signature_key_pairs: Table,
    encryption_key_pairs: Table,
    key_packages: Table,
    psks: Table,

    // extensions-draft
    application_export_tree: Table,

    // virtual-clients-draft
    vc_emulation_epoch_state: Table,
    vc_emulation_bindings: Table,
    registered_vc_emulation_epoch: Table,
    vc_operation_tree: Table,
    retained_key_package_material: Table,
    retained_key_package_epoch: Table,
}

#[derive(Default)]
pub struct StorageProviderState(Arc<Mutex<Data>>);

impl StorageProviderState {
    pub fn as_postcard_provider(&self) -> PostcardProvider<'_> {
        PostcardProvider(self)
    }
    pub fn as_serde_json_provider(&self) -> SerdeJsonProvider<'_> {
        SerdeJsonProvider(self)
    }
}

// The previous-version crypto/rand provider is a local wrapper around the current
// libcrux provider (see `crate::test_crypto_provider`), aliased here to the name
// the previous-version libcrux crate used, so it drops in unchanged below.
use crate::test_crypto_provider as openmls_libcrux_crypto_compat;

#[cfg(feature = "storage_migration_0_8")]
use openmls_traits_0_5_0 as openmls_traits_compat;

#[cfg(feature = "storage_migration_0_7")]
use openmls_traits_0_4_1 as openmls_traits_compat;

/// A full [`OpenMlsProvider`](openmls_traits_compat::OpenMlsProvider) for the
/// `0.8.1` API that pairs a [`PostcardProvider`] (storage) with a libcrux-backed
/// crypto/rand provider. This is what group operations run against while the
/// group state is set up before a migration.
pub struct PostcardOpenMlsProvider<'a> {
    storage: &'a PostcardProvider<'a>,
    crypto: openmls_libcrux_crypto_compat::CryptoProvider,
}

pub struct PostcardProvider<'a>(&'a StorageProviderState);

impl<'a> PostcardProvider<'a> {
    pub fn as_openmls_provider(&'a self) -> PostcardOpenMlsProvider<'a> {
        PostcardOpenMlsProvider {
            storage: self,
            crypto: openmls_libcrux_crypto_compat::CryptoProvider::new()
                .expect("failed to initialize libcrux crypto provider"),
        }
    }
}
pub struct SerdeJsonProvider<'a>(&'a StorageProviderState);

/// A full [`OpenMlsProvider`](openmls_traits::OpenMlsProvider) for the *current*
/// API that pairs a [`SerdeJsonProvider`] (storage) with a libcrux-backed
/// crypto/rand provider. This is used to drive a migrated group through real
/// operations.
pub struct SerdeJsonOpenMlsProvider<'a> {
    storage: &'a SerdeJsonProvider<'a>,
    crypto: openmls_libcrux_crypto_current::CryptoProvider,
}

/// A full [`OpenMlsProvider`](openmls_traits_compat::OpenMlsProvider) for the *current*
/// API that pairs a [`SerdeJsonProvider`] (storage) with a libcrux-backed
/// crypto/rand provider. This is used to drive a migrated group through real
/// operations.
pub struct SerdeJsonOpenMlsProviderCompat<'a> {
    storage: &'a SerdeJsonProvider<'a>,
    crypto: openmls_libcrux_crypto_compat::CryptoProvider,
}

impl<'a> SerdeJsonProvider<'a> {
    pub fn as_openmls_provider(&'a self) -> SerdeJsonOpenMlsProvider<'a> {
        SerdeJsonOpenMlsProvider {
            storage: self,
            crypto: openmls_libcrux_crypto_current::CryptoProvider::new()
                .expect("failed to initialize libcrux crypto provider"),
        }
    }
}

impl<'a> SerdeJsonProvider<'a> {
    pub fn as_openmls_provider_compat(&'a self) -> SerdeJsonOpenMlsProviderCompat<'a> {
        SerdeJsonOpenMlsProviderCompat {
            storage: self,
            crypto: openmls_libcrux_crypto_compat::CryptoProvider::new()
                .expect("failed to initialize libcrux crypto provider"),
        }
    }
}

